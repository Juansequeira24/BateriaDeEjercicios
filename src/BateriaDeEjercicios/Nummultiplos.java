public class Nummultiplos {

	public static void main(String[] args) {
		
		int i =0;
		
		while(i++<=100) {
			System.out.println(i);
			if(i % 2==0) {
				System.out.println("Es multiplo de 2.");
			}
			
		}
		System.out.println("Fin del programa");
		
		

	}

}
